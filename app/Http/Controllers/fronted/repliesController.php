    {
        $this->validate($request,[
            'reply' => 'required'
        ]);

        $reply = new Reply();
        $data = $request -> all();


        $reply -> content = $data['reply'];
        $reply -> comment_id = $data['comment_id'];

     try {
         DB::beginTransaction();
        $reply -> save();
        DB::commit();
        //$this->CreateMessages('done');
        } catch (\Exception $e) {
            DB::rollback();
          //  $this->CreateMessages('not_done');
            throw $e;
        }
        
        return redirect()->back();
    }

